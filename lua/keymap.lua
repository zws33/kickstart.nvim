-- Replace all buffer contents with clipboard
vim.keymap.set('n', '<leader>ra', 'ggVG"+p', { desc = 'Replace all with clipboard' })

-- Open cheatsheet in floating window
vim.keymap.set('n', '<leader>?', function()
  local buf = vim.api.nvim_create_buf(false, true)
  local width = math.floor(vim.o.columns * 0.8)
  local height = math.floor(vim.o.lines * 0.8)
  vim.api.nvim_open_win(buf, true, {
    relative = 'editor',
    width = width,
    height = height,
    col = math.floor((vim.o.columns - width) / 2),
    row = math.floor((vim.o.lines - height) / 2),
    style = 'minimal',
    border = 'rounded',
  })
  vim.cmd 'read ~/.config/nvim/CHEATSHEET.md'
  vim.api.nvim_buf_set_lines(buf, 0, 1, false, {})
  vim.bo[buf].modifiable = false
  vim.bo[buf].filetype = 'markdown'
  vim.keymap.set('n', 'q', '<cmd>close<CR>', { buffer = buf })
end, { desc = 'Open cheatsheet' })

